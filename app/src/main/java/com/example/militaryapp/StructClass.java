package com.example.militaryapp;

public class StructClass {
    String title;
    String text;

    public StructClass() {
    }

    public StructClass(String title, String text) {
        this.title = title;
        this.text = text;
    }

    public String getTitle() {
        return title;
    }

    public String getText() {
        return text;
    }

}
