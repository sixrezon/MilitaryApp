package com.example.militaryapp;

public class WeaponsClass {
    String title;
    String text;

    public WeaponsClass() {
    }

    public WeaponsClass(String title, String text) {
        this.title = title;
        this.text = text;
    }

    public String getTitle() {
        return title;
    }

    public String getText() {
        return text;
    }

}
